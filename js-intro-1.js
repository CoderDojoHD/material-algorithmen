// Mit Javascript kann man einfach rechnen und Ausgaben machen
1 + 1

1 / 3

132 * (5 + 9) / 33.3

console.log("Das Ergebnis lautet:", 100)


// Werte kann man sich in Variablen merken

var zwei = 1 + 1

var fuenf = zwei * zwei + 1

console.log("Hier kommt die", fuenf)


// Variablen können verändert werden

zwei = 3

fuenf = fuenf + zwei

console.log("Hier kommt nochmal die", fuenf)


// Es gibt auch Listen von Werten, Arrays genannt

var zahlen = [1, 2, 3]
var summe = zahlen[0] + zahlen[1] + zahlen[2]
console.log("Die Summe aller Zahlen =", summe)


// Auch Arrays können verändert werden

zahlen[0] = 100
zahlen.push(200)
console.log("zahlen =", zahlen)
console.log("100 + 200 =", zahlen[0] + zahlen[3])
